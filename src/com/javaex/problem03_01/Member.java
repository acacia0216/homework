package com.javaex.problem03_01;

public class Member {
	private String id, name;
	private int point;
	
	public void setId(String id) {
		this.id = id;
	}
	public void setName(String name) {
		this.name = name;
	}
	public void setPoint(int point) {
		this.point = point;
	}
	public String getId() {
		return this.id;
	}
	public String getName() {
		return this.name;
	}
	public int getPoint() {
		return this.point;
	}
	
	
}
